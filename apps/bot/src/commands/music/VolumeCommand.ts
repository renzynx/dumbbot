import { SlashCommandBuilder  } from "discord.js";
import { Command } from "@/core/Command";
import type { BotClient } from "@/core/Client";
import type { CommandContext } from "@/core/Context";
import { SlashCommand, GuildOnly } from "@/decorators";

@SlashCommand("volume", "Set the volume")
@GuildOnly()
export class VolumeCommand extends Command {
  constructor(client: BotClient) {
    super(client);
  }

  override build() {
    return new SlashCommandBuilder()
      .setName(this.name)
      .setDescription(this.description)
      .addIntegerOption((option) =>
        option
          .setName("level")
          .setDescription("Volume level (0-150)")
          .setMinValue(0)
          .setMaxValue(150)
          .setRequired(true)
      );
  }

  async execute(ctx: CommandContext): Promise<void> {
    const music = this.client.music;
    if (!music) {
      await ctx.reply({
        embeds: [ctx.error("Music system is not available!")],
        ephemeral: true,
      });
      return;
    }

    const queue = music.queues.get(ctx.guild!.id);
    if (!queue?.current) {
      await ctx.reply({
        embeds: [ctx.error("Nothing is playing!")],
        ephemeral: true,
      });
      return;
    }

    const volume = ctx.interaction.options.getInteger("level", true);
    await music.setVolume(ctx.guild!.id, volume);

    await ctx.reply({
      embeds: [ctx.success(`Volume set to **${volume}%**`)],
    });
  }
}
